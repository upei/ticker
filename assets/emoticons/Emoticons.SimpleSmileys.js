// Build a regular expression containing all emoticons in this set.
if (!Emoticons.SimpleSmileys.regexp) {
    Emoticons.SimpleSmileys.regexp = Emoticons.buildRegexp(Emoticons.SimpleSmileys.mappings);
}

// Convert emoticons to <img /> using then Emoticons.convertEmoticons() function.
Emoticons.SimpleSmileys.convertEmoticons = function(msg) {
    return Emoticons.convertEmoticons(
        msg,
        Emoticons.SimpleSmileys.regexp,
        Emoticons.SimpleSmileys.mappings,
        Emoticons.SimpleSmileys.className
    );
}